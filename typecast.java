import java.util.Scanner;
class typecast{
	public static void main(String [] args){
		int a=10;
		float b=a;
		System.out.println("Int to Float:"+ b);
		float f=10.2f;
		System.out.println("float:"+ f);
		double q=3.2315467856534;
		float d=(float)q;
		System.out.println("Double to Float"+ d);
		
	}
}